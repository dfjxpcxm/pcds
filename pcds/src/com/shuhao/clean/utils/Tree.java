package com.shuhao.clean.utils;

public class Tree {
	private String node_id;
	private String node_name;
	private String parent_node_id;
	private String node_handler;
	private String node_image;
	
	
	public String getNode_id() {
		return node_id;
	}
	public void setNode_id(String node_id) {
		this.node_id = node_id;
	}
	public String getNode_name() {
		return node_name;
	}
	public void setNode_name(String node_name) {
		this.node_name = node_name;
	}
	public String getParent_node_id() {
		return parent_node_id;
	}
	public void setParent_node_id(String parent_node_id) {
		this.parent_node_id = parent_node_id;
	}
	public String getNode_handler() {
		return node_handler;
	}
	public void setNode_handler(String node_handler) {
		this.node_handler = node_handler;
	}
	public String getNode_image() {
		return node_image;
	}
	public void setNode_image(String node_image) {
		this.node_image = node_image;
	}
	
	
}
